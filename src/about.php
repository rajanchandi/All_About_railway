<a><h2 style="margin-top:5px">About us:</h2></div></a><br><br>
<h3 style="padding:10px">ALL ABOUT RAILWAY is an integral part of Integrated Coaching Management System developed and maintained by AAR. Although Indian Railways make all out efforts to run all passenger carrying trains as per their schedules and maintain their punctuality , at times for reasons beyond the control of Indian Railways trains get delayed, are rescheduled from their starting stations, cancelled or diverted to another route resulting in change in the actual Arrival/Departure time from their scheduled time. To save the inconvenience caused to Rail users due to these changes in train running, National Train Enquiry System (NTES) provides information to public about expected Arrival/Departure of trains at each stopping station, train schedule information, information about cancelled trains, diverted trains, and also platform berthing information at major stations.

The main goal and objective behind NTES system to provide timely and reliable information to general public through user friendly interfaces and PAN India accessibility has been achieved to a large extent and now the information is conveniently and reliably available to public all over the country through various delivery channels i.e. through web browsing, through mobile phone or landline (voice and SMS) and also in person through face to face enquiry and displays at all IR stations. NTES has won National Award for e-Governance 2010-2011 for Innovative use of ICT by PSUs for customer's benefit and “National Train Enquiry System (NTES)” was adjudged as a Finalist in the Computerworld Honors Program instituted by the Computerworld Information Technology Awards
